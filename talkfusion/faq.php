			<hr>
			<b>Dividas Referente a Empresa TALK FUSION</b><br>
			<b class="gratuito"> 1 - Como serei pago instantaneamente ?</b>
            <p><strong>Venda hoje e sua comiss&atilde;o ser&aacute; instantaneamente acreditada na sua conta E-Wallet, e com o cart&atilde;o INTERNACIONAL da empresa voc&ecirc; poder&aacute; utilizar seu saldo de imediato em qualquer lugar do mundo que aceite VISA. </strong></p>
			
			<b class="gratuito"> 2 - Como posso abrir uma conta E-Wallet ?</b>
            <p><strong>Ap&oacute;s ganhar a sua primeira Comiss&atilde;o, a sua conta E-Wallet ser&aacute; automaticamente aberta em 5 dias &uacute;teis. Depois de criada, voc&ecirc; receber&aacute; um e-mail de confirma&ccedil;&atilde;o da <a href="http://talkfusion.com/pt/" title="Site Oficial da empresa" target="_blank">TALK FUSION</a>, mais um e-mail da PAYLUTION® sobre como ativar sua conta. Todas as Comiss&atilde;es ganhas ir&atilde;o imediatamente para a sua conta E-Wallet. </strong></p>
			
			<b class="gratuito"> 3 - Como posso qualificar-me e permanecer qualificado a fim de receber as Comiss&otilde;es de Equipe ?</b>
            <p><strong>A fim de qualificar e permanecer qualificado para receber as Comiss&otilde;es de Equipe, voc&ecirc; deve ter ao menos 1 indicado direto em cada lado de sua equipe (um na esquerda e outro direita) e manter sua mensalidade em dia. </strong></p>
			
			<b class="gratuito"> 4. Se sou um Associado com o pacote PRO-PAK, isso me permite receber maior volume de vendas das vendas de produtos dentro da minha organiza&ccedil;&atilde;o ?</b>
            <p><strong>Sim. Associados com pacote PRO-PAK ganhar&atilde;o uma maior pontua&ccedil;&atilde;o de vendas de produtos na organiza&ccedil;&atilde;o</strong></p>
			<p><i class="red">Exemplo:</i> Se o associado estiver com o pacote PRO-PAK, a cada pacote que entrar na rede dele somar&aacute; pontos;</p>
			<b>Iniciante (50 pontos na equipe)</b><br> 
			<b>Executivo (100 pontos na equipe)</b><br>
			<b>Elite (300 pontos na equipe)</b><br>
			<b>Pacote Pro (600 pontos na equipe)</b><br> 
			<p>Se o associado estiver com o pacote ELITE, a cada pacote que entrar na rede dele somar&aacute; pontos;</p>
			<b>Iniciante (50 pontos na equipe)</b><br>
			<b>Executivo (100 pontos na equipe)</b><br>
			<b>Elite (300 pontos na equipe)</b><br>
			<b>Pacote Pro (300 pontos na equipe)</b><br> 
			<p>Se o associado estiver com o pacote EXECUTIVE, a cada pacote que entrar na rede dele somar&aacute; pontos;</p>
			<b>Iniciante (50 pontos na equipe)</b><br>
			<b>Executivo (100 pontos na equipe)</b><br>
			<b>Elite (100 pontos na equipe)</b><br>
			<b>Pacote Pro (100 pontos na equipe)</b><br> 
			<p>Se o associado estiver com o pacote INICIANTE, a cada pacote que entrar na rede dele somar&aacute; pontos;</p>
			<b>Iniciante (50 pontos na equipe)</b><br>
			<b>Executivo (50 pontos na equipe)</b><br>
			<b>Elite (50 pontos na equipe)</b><br>
			<b>Pacote Pro (50 pontos na equipe)</b><br> 
			<p>Lembrando que a cada 100 pontos em cada lado(100 na equipe esquerda e 100 na equipe direita) voc&ecirc; ganhar&aacute; <b class="gratuito">$25,00</b> d&oacute;lares.</p>
			<p>E quando uns dos afiliados abiaxo efetuarem o UP-GRADE para algum dos pacotes superiores, a diferen&ccedil;a de pontua&ccedil;&otilde;es ser&atilde;o somadas	.</p>
			<b class="gratuito"> 5 - Possuo um limite para meu potencial de ganho semanal ?</b>
            <p><strong>SIM </strong></p>
			<b>Associados Iniciantes podem ganhar um m&aacute;ximo de <i class="red">US$ 500</i> por semana em Comiss&otilde;es de equipe. </b>
			<b>Associados Executivos podem ganhar um m&aacute;ximo de <i class="red">US$ 1.000</i> por semana em Comiss&otilde;es de equipe.</b>
			<b>Associados Elite podem ganhar um m&aacute;ximo de <i class="red">US$ 25.000</i> por semana em Comiss&otilde;es de equipe.</b>
			<b>Associados Pro-PAK podem ganhar um m&aacute;ximo de <i class="red">US$ 50.000</i> por semana em Comiss&otilde;es de equipe.</b>
			<p><i>N&atilde;o h&aacute; limite no B&ocirc;nus de In&iacute;cio R&aacute;pido, B&ocirc;nus de Correspond&ecirc;ncia e Associa&ccedil;&atilde;o de Liderança.</i></p>
			
			<b class="gratuito"> 6 - O que acontece com minha pontua&ccedil;&atilde;o(Volume de Vendas) se eu ficar inativo ?</b>
            <p> Depois de 30 dias de inatividades toda suas pontua&ccedil;&otilde;es de  Volume de Vendas voltam ao zero. As pontua&ccedil;&otilde;es de Volume de Vendas perdidas n&atilde;o ser&atilde;o preservados; entretanto, voc&ecirc; poder&aacute; reativ&aacute;-las ao gerar pessoalmente um m&iacute;nimo de 10 pontos de Volumes de Vendas Pessoal e mant&ecirc;-lo numa base mensal(a mensalidade automaticamente j&aacute; ir&aacute; gerar 20 pontos mensalmente).</p> 
			<p>Depois de 90 dias de inatividade, seu status de S&oacute;cio ser&aacute; cancelado e sua conta ser&aacute; fechada.</p>
			
			<b class="gratuito"> 7 - Quando posso ganhar o B&ocirc;nus Categoria Avan&ccedil;o ?</b>
            <p>Este b&ocirc;nus &eacute; ganho quando voc&ecirc; atinge a categoria espec&iacute;fica para a primeira equipe e a mant&eacute;m por 2 semanas consecutivas. Todos os B&ocirc;nus Avan&ccedil;o s&atilde;o pagos no d&eacute;cimo quinto dia do m&ecirc;s seguinte.</p>
			
			<b class="gratuito"> 8 - Posso ganhar todos os 4 B&ocirc;nus de Avan&ccedil;o num per&iacute;odo de 2 semanas ?</b>
            <p>N&atilde;o. Para receber um B&ocirc;nus de Avan&ccedil;o, voc&ecirc; deve qualificar para uma categoria espec&iacute;fica por 2 semanas consecutivas. Assim, levaria no m&iacute;nimo 8 semanas para qualificar para todas os 4 B&ocirc;nus Avan&ccedil;o.</p>
			
 
			<b class="gratuito"> 9 - Como posso ganhar um B&ocirc;nus do Fundo de Lideran&ccedil;a ?</b>	
			<p>A fim de qualificar para o B&ocirc;nus do Fundo de Lideran&ccedil;a, voc&ecirc; deve atingir os requisitos de pontua&ccedil;&atilde;o(Volume de Vendas) da categoria durante 4 semanas do m&ecirc;s. As Comiss&otilde;es do Fundo de Lideran&ccedil;a ser&atilde;o pagas no dia 20 do m&ecirc;s seguinte ao m&ecirc;s em que voc&ecirc; se qualificou.</p>
			
			<b class="gratuito"> 10 -  Quantas fra&ccedil;&otilde;es do Fundo de Lideran&ccedil;a pode um S&oacute;cio ganhar ?</b>	
			<p>Um S&oacute;cio individual pode ganhar um m&aacute;ximo de 25% do Fundo de Lideran&ccedil;a. Por exemplo, se o total do Fundo de Lideran&ccedil;a &eacute; US$10,000 e h&aacute; apenas um S&oacute;cio Qualificado, este S&oacute;cio &eacute; eleg&iacute;vel para o total de US$2,500. Se houver 4 ou mais S&oacute;cios Qualificados, eles dividir&atilde;o igualmente o total do montante do Fundo de Lideran&ccedil;a. Em outras palavras, 10 S&oacute;cios Qualificados ganhariam cada um US$1,000. </p>
			
			<b class="gratuito"> 11 - Qual &eacute; a diferen&ccedil;a entre a Comiss&atilde;o de Categoria e Reconhecimento de Categoria ?</b>	
			<p>A Comiss&atilde;o de Categoria &eacute; a categoria na qual voc&ecirc; est&aacute; sendo pago e &eacute; determinada com base no montante do Volume de Vendas e outros requisitos do Plano de Compensa&ccedil;&atilde;o em qualquer Comiss&atilde;o Semanal. Isto pode variar de acordo com seu Reconhecimento de Categoria, o qual &eacute; a categoria mais alta para a qual voc&ecirc; j&aacute; foi pago.</p>
			
			<b class="gratuito"> 12 - Posso ter mais de um pacote ?</b>	
			<p>SIM, E quando voc&ecirc; atingir a categoria de "Diamante" para a primeira equipe, dois pacotes de cortesia, um em sua equipe esquerdo e outro em sua equipe direito, ser&atilde;o colocados a frente do seu pacote original que se tornou Diamante, isso far&aacute; com que seus novos pacotes tamb&eacute;m somem pontua&ccedil;&atilde;o de sua equipe j&aacute; existente. Os novos pacotes come&ccedil;ar&atilde;o a colher pontua&ccedil;&otilde;es a partir do dia em que foram criados. POR&Eacute;M ESSES 2 NOVOS PACOTES N&atilde;o FAR&Atilde;O PARTE DE NOSSA ESTRAT&Eacute;GIA, POIS O CONCEITO DE NOSSA ESTRAT&Eacute;GIA &Eacute; SEMPRE DAR SEGUIMENTO ABAIXO DE NOSSA EQUIPE, PARA QUE TODOS TENHAM LUCRATIVIDADE EM NOSSA EQUIPE.</p>
			
			<b class="gratuito"> 13 - H&aacute; alguma taxa pelas Comiss&otilde;es ganhas ?</b>	
			<p>Informamos que uma taxa de US$0.30 ser&aacute; deduzida todas as vezes que a sua Comiss&atilde;o de equipe for enviada para a sua conta E-Wallet</p>
			
			<b class="gratuito"> 14 - Uma compra de produto &eacute; exigida para se tornar um S&oacute;cio Independente da Talk Fusion ? </b>	
			<p>A compra de um produto n&atilde;o &eacute; necess&aacute;ria para tornar-se um S&oacute;cio Independente e participar do Plano de Compensa&ccedil;&atilde;o, a TALK FUSION tem o pacote de ades&atilde;o que custa no valor de U$ 30,00 d&oacute;lares.</p>
			
			<b class="gratuito"> 15 - Todos os pa&iacute;ses pagam uma taxa de assinatura mensal ?</b>	
			<p>Sim. Todos os Associados - quem compram um produto - e clientes em todos os pa&iacute;ses pagam uma taxa de assinatura mensal. Isto permite a todos os Associados, n&atilde;o importa onde residem, se qualificarem e ganharem a Renda Residual. Todos os Associados devem pessoalmente gerar um m&iacute;nimo de 10 Pontos(Volume de Vendas Pessoais "PSV") mensalmente para manter uma posi&ccedil;&atilde;o Ativa(sendo que ao pagar sua mensalidade de U$20 a U$35 d&oacute;lares, voc&ecirc; j&aacute; estar&aacute; automaticamente somando 20 pontos para se manter ativo).</p>
			
			<b class="gratuito"> 16 - Existe alguma maneira para economizar a minha taxa de assinatura mensal ? </b>	
			<p>Sim. Os Associados e Clientes podem escolher uma op&ccedil;&atilde;o de pagamento anual Pr&eacute;-Pago/Pago Integralmente e economizar 10% no custo da assinatura mensal. O pagamento anual Pr&eacute;-Pago/Pago Integralmente n&atilde;o pode ser cancelado ou reembolsado, a menos que tenha sido adquirido juntamente com um Pacote de Produto e cancelado nos 3 primeiros dias.</p>
			 
