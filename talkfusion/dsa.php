			<h2>DSA - DIRECT SELLING ASSOCIATION</h2>
			<p>Associa&ccedil;&atilde;o de Vendas Diretas (DSA) &eacute; o nome dado &agrave; associa&ccedil;&otilde;es comerciais semelhantes nos Estados Unidos, Reino Unido, Austr&aacute;lia, Mal&aacute;sia, Cingapura e Nova Zel&acirc;ndia, que representam empresas de vendas diretas, principalmente aqueles que usam planos de compensa&ccedil;&atilde;o de marketing multin&iacute;vel. Em nome das empresas associadas, a DSA se engaja nas rela&ccedil;&otilde;es p&uacute;blicas de regulamenta&ccedil;&atilde;o do setor de marketing multi-n&iacute;vel e financia a candidatos pol&iacute;ticos atrav&eacute;s de um comit&ecirc; de a&ccedil;&atilde;o pol&iacute;tica.</p>

			<p>Hist&oacute;ria da DSA nos Estados Unidos</p>

			<p>A DSA norte-americana, com sede em Washington, &eacute; a associa&ccedil;&atilde;o comercial nacional de um grupo de empresas que fabricam e distribuem bens e servi&ccedil;os vendidos diretamente aos consumidores normalmente atrav&eacute;s de marketing multin&iacute;vel.</p>

			<p>Fundada em Binghamton, Nova York, em 1910, a associa&ccedil;&atilde;o foi originalmente chamada de Associa&ccedil;&atilde;o de Agentes de Cr&eacute;dito. Tornou-se a Direct Selling Association, em 1968. Em 1970, menos de 5% dos membros da DSA eram empresas de marketing multin&iacute;vel. Entre 2009-2011, a DSA passou a ter aproximadamente 200 empresas associadas, sendo mais de 90% das quais, eram empresas de marketing multin&iacute;vel.</p>

			<p>A DSA pertence &agrave; Federa&ccedil;&atilde;o Nacional de Varejo e as empresas associadas devem cumprir o rigoroso c&oacute;digo de &eacute;tica da DSA.</p>

